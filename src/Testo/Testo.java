/*Universo_DB – Catalogo Strutturato dell’Universo:
Introduzione e idea del progetto

Il progetto “Universo_DB” nasce con l’obiettivo di realizzare un database relazionale che rappresenti l’Universo come un sistema strutturato e interconnesso, ispirato ai cataloghi utilizzati in ambito astronomico e scientifico.

Nel corso dello sviluppo, l’idea iniziale del progetto ha subito diverse evoluzioni e adattamenti, portando a una definizione più concreta e realistica del modello finale. L’attenzione si è spostata dalla semplice rappresentazione teorica dell’Universo alla costruzione di un database coerente, funzionale e ben organizzato, capace di gestire oggetti cosmici, eventi e osservazioni in modo relazionale.

L’astronomia moderna non studia i corpi celesti come elementi isolati, ma li analizza all’interno di strutture più ampie e in relazione tra loro. Partendo da questa osservazione, “Universo_DB” è stato progettato per riflettere questa complessità, pur mantenendo un livello di dettaglio adeguato a un progetto scolastico.

Il progetto è stato sviluppato seguendo il modello a tre strati affrontato durante il corso:

- Strato di presentazione: applicazione Java con interfaccia a riga di comando;
- Strato logico: classi Java che gestiscono la logica applicativa e l’accesso ai dati;
- Strato dati: database relazionale MySQL progettato tramite MySQL Workbench.

-------------------------------------------------------------------------------------------------------------
Obiettivo del progetto

L’obiettivo principale del progetto è la realizzazione di un database relazionale in grado di:

- rappresentare la struttura dell’Universo attraverso entità collegate tra loro, come galassie e oggetti cosmici;
- descrivere le caratteristiche principali dei corpi celesti, come stelle, pianeti e buchi neri;
- modellare eventi cosmici che avvengono nel tempo;
- collegare gli oggetti cosmici alle osservazioni scientifiche e agli strumenti utilizzati;
- applicare correttamente i concetti fondamentali dei database relazionali, come chiavi primarie, chiavi esterne, relazioni, tipi ENUM e gestione delle date.

Il database non è quindi solo descrittivo, ma anche relazionale, in quanto permette di mettere in relazione entità diverse e di rappresentare legami logici tra fenomeni astronomici e attività di osservazione.

-------------------------------------------------------------------------------------------------------------
Idea di fondo e approccio progettuale

L’idea di fondo è quella di costruire una struttura gerarchica e coerente, partendo dagli elementi più generali fino ad arrivare ai singoli oggetti e alle osservazioni:

- l’Universo viene rappresentato attraverso galassie;
- ogni galassia può contenere diversi oggetti cosmici;
- gli oggetti cosmici includono stelle, pianeti, buchi neri e altri corpi celesti;
- le stelle possono essere al centro di sistemi stellari con pianeti e satelliti;
- nel tempo possono verificarsi eventi cosmici rilevanti;
- gli oggetti e gli eventi vengono studiati tramite osservazioni scientifiche effettuate con strumenti specifici.

Questo approccio consente di ottenere un modello chiaro, coerente con la realtà scientifica e sufficientemente strutturato, evitando però complessità non necessarie.

-------------------------------------------------------------------------------------------------------------
Motivazione della scelta del tema

La scelta del tema “Spazio e Universo” è motivata da diversi fattori:

- l’elevata varietà di entità modellabili;
- la presenza di numerose relazioni tra le entità;
- la possibilità di applicare in modo concreto i concetti teorici studiati sui database relazionali;
- l’interesse scientifico e culturale dell’argomento;
- l’opportunità di sviluppare un progetto articolato ma controllabile, senza perdere coerenza.

-------------------------------------------------------------------------------------------------------------
Struttura generale del progetto

Il progetto è stato sviluppato seguendo una suddivisione logica delle entità principali:

1. Galassie e struttura generale dell’Universo;
2. Oggetti cosmici;
3. Sistemi stellari e pianeti;
4. Eventi cosmici;
5. Osservazioni scientifiche e strumenti;
6. Missioni spaziali e partecipazione umana.

Ogni sezione è stata integrata nel database e collegata alle altre tramite relazioni appropriate, garantendo consistenza e integrità dei dati.

-------------------------------------------------------------------------------------------------------------
Conclusione

“Universo_DB” rappresenta un’applicazione pratica delle conoscenze acquisite durante il corso di database e programmazione Java. Il progetto dimostra la capacità di progettare e realizzare un database relazionale coerente, strutturato e funzionale, partendo da un’idea iniziale e adattandola progressivamente alle esigenze concrete dello sviluppo.

Il risultato finale è un sistema che, pur rimanendo nei limiti di un progetto scolastico, riproduce in modo realistico l’organizzazione di un archivio astronomico semplificato.
*/
